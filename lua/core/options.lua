vim.cmd "language en_US"
vim.cmd "set nowrap"
vim.cmd "set backspace=indent,eol,start"
vim.cmd "set relativenumber"
vim.cmd "set number"
vim.cmd "set cursorline"
vim.cmd "set encoding=utf-8"
vim.cmd "set fileencoding=utf-8"

vim.bo.tabstop = 2       -- Visual tab width
vim.bo.shiftwidth = 2    -- Indentation width for auto-indent
vim.bo.softtabstop = 2   -- How many spaces a tab counts for during editing
vim.bo.expandtab = true  -- Use spaces instead of actual tab characters
vim.o.tabstop = 2
vim.o.shiftwidth = 2
vim.o.softtabstop = 2
vim.o.expandtab = true
vim.o.incsearch = true
vim.o.hlsearch = false
vim.o.clipboard = "unnamedplus"

